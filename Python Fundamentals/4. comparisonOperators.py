# We assign the value 5 to variable x
x = 5

# We assign the value 8 to variable y
y = 8

# This checks if x is equal to y (5 == 8), which is False
print(x == y)   # False

# This checks if x is NOT equal to y (5 != 8), which is True
print(x != y)   # True

# This checks if x is less than y (5 < 8), which is True
print(x < y)    # True

# This checks if x is greater than y (5 > 8), which is False
print(x > y)    # False

# This checks if x is less than or equal to y (5 <= 8), which is True
print(x <= y)   # True

# This checks if x is greater than or equal to y (5 >= 8), which is False
print(x >= y)   # False
