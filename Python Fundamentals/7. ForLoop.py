# Loop from 0 to 4 (5 is not included)
for i in range(5):  
    print("Loop:", i)  # Output: 0, 1, 2, 3, 4

print()  # Print a blank line

# Loop from 3 to 7 (8 is not included)
for i in range(3, 8):  
    print("Loop2:", i)  # Output: 3, 4, 5, 6, 7

print()  # Print a blank line

# Loop from 8 down to 3 (stop before 2), step by -2
for i in range(8, 2, -2):  
    print("Loop3:", i)  # Output: 8, 6, 4
